"use client";

import { useState, useEffect } from "react";
import { motion } from "framer-motion";
import { Camera, Mail, Lock, User, ArrowRight, Loader2 } from "lucide-react";
import { useAuth } from "@/lib/auth-context";
import { useRouter, useSearchParams } from "next/navigation";
import { appTranslations, AppLanguage, getStoredLanguage, setStoredLanguage } from "@/lib/translations";

export default function LoginPage() {
    const [mode, setMode] = useState<"login" | "signup">("login");
    const [email, setEmail] = useState("");
    const [password, setPassword] = useState("");
    const [fullName, setFullName] = useState("");
    const [loading, setLoading] = useState(false);
    const [error, setError] = useState<string | null>(null);
    const [lang, setLang] = useState<AppLanguage>('en');

    const { signInWithGoogle, signInWithEmail, signUpWithEmail } = useAuth();
    const router = useRouter();
    const searchParams = useSearchParams();

    // Initialize language from URL or localStorage
    useEffect(() => {
        const urlLang = searchParams.get('lang');
        if (urlLang === 'es' || urlLang === 'en') {
            setLang(urlLang);
            setStoredLanguage(urlLang);
        } else {
            setLang(getStoredLanguage());
        }
    }, [searchParams]);

    const t = appTranslations[lang];

    const handleGoogleSignIn = async () => {
        setLoading(true);
        setError(null);
        try {
            await signInWithGoogle();
        } catch (err) {
            setError(t.login.errors.google);
            console.error(err);
        } finally {
            setLoading(false);
        }
    };

    const handleEmailAuth = async (e: React.FormEvent) => {
        e.preventDefault();
        setLoading(true);
        setError(null);

        try {
            if (mode === "login") {
                await signInWithEmail(email, password);
                router.push("/");
            } else {
                await signUpWithEmail(email, password, fullName);
                setError(t.login.errors.checkEmail);
            }
        } catch (err: unknown) {
            const errorMessage = err instanceof Error ? err.message : t.login.errors.auth;
            setError(errorMessage);
        } finally {
            setLoading(false);
        }
    };

    return (
        <div className="min-h-screen flex flex-col items-center justify-center px-6 py-12 aurora-bg">
            {/* Logo */}
            <motion.div
                className="flex items-center gap-3 mb-8"
                initial={{ opacity: 0, y: -20 }}
                animate={{ opacity: 1, y: 0 }}
            >
                <div className="w-12 h-12 rounded-xl bg-gradient-to-br from-blue-500 to-cyan-400 flex items-center justify-center">
                    <Camera className="w-7 h-7 text-white" />
                </div>
                <span className="text-2xl font-bold">SiteSnap AI</span>
            </motion.div>

            {/* Card */}
            <motion.div
                className="glass-card w-full max-w-md p-8"
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: 0.1 }}
            >
                <h1 className="text-2xl font-bold text-center mb-2">
                    {mode === "login" ? t.login.welcomeBack : t.login.createAccount}
                </h1>
                <p className="text-gray-400 text-center mb-8">
                    {mode === "login"
                        ? t.login.signInToContinue
                        : t.login.startOrganizing}
                </p>

                {/* Google Button */}
                <button
                    onClick={handleGoogleSignIn}
                    disabled={loading}
                    className="w-full flex items-center justify-center gap-3 bg-white text-gray-900 font-semibold py-4 rounded-xl mb-6 hover:bg-gray-100 transition-colors disabled:opacity-50"
                >
                    <svg className="w-5 h-5" viewBox="0 0 24 24">
                        <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                        <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                        <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
                        <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
                    </svg>
                    {t.login.continueWithGoogle}
                </button>

                {/* Divider */}
                <div className="flex items-center gap-4 mb-6">
                    <div className="flex-1 h-px bg-white/10" />
                    <span className="text-gray-500 text-sm">{t.login.orWithEmail}</span>
                    <div className="flex-1 h-px bg-white/10" />
                </div>

                {/* Email Form */}
                <form onSubmit={handleEmailAuth} className="space-y-4">
                    {mode === "signup" && (
                        <div className="relative">
                            <User className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-gray-500" />
                            <input
                                type="text"
                                placeholder={t.login.fullName}
                                value={fullName}
                                onChange={(e) => setFullName(e.target.value)}
                                required
                                className="w-full bg-white/5 border border-white/10 rounded-xl py-4 pl-12 pr-4 text-white placeholder:text-gray-500 focus:border-blue-500 focus:outline-none transition-colors"
                            />
                        </div>
                    )}

                    <div className="relative">
                        <Mail className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-gray-500" />
                        <input
                            type="email"
                            placeholder={t.login.email}
                            value={email}
                            onChange={(e) => setEmail(e.target.value)}
                            required
                            className="w-full bg-white/5 border border-white/10 rounded-xl py-4 pl-12 pr-4 text-white placeholder:text-gray-500 focus:border-blue-500 focus:outline-none transition-colors"
                        />
                    </div>

                    <div className="relative">
                        <Lock className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-gray-500" />
                        <input
                            type="password"
                            placeholder={t.login.password}
                            value={password}
                            onChange={(e) => setPassword(e.target.value)}
                            required
                            minLength={6}
                            className="w-full bg-white/5 border border-white/10 rounded-xl py-4 pl-12 pr-4 text-white placeholder:text-gray-500 focus:border-blue-500 focus:outline-none transition-colors"
                        />
                    </div>

                    {error && (
                        <p className={`text-sm ${error.includes("email") || error.includes("Check") ? "text-green-400" : "text-red-400"}`}>
                            {error}
                        </p>
                    )}

                    <button
                        type="submit"
                        disabled={loading}
                        className="w-full btn-primary flex items-center justify-center gap-2 disabled:opacity-50"
                    >
                        {loading ? (
                            <Loader2 className="w-5 h-5 animate-spin" />
                        ) : (
                            <>
                                {mode === "login" ? t.login.signIn : t.login.createAccountBtn}
                                <ArrowRight className="w-5 h-5" />
                            </>
                        )}
                    </button>
                </form>

                {/* Toggle Mode */}
                <p className="text-center text-gray-400 mt-6 text-sm">
                    {mode === "login" ? (
                        <>
                            {t.login.noAccount}{" "}
                            <button
                                onClick={() => setMode("signup")}
                                className="text-cyan-400 hover:underline"
                            >
                                {t.login.signUpFree}
                            </button>
                        </>
                    ) : (
                        <>
                            {t.login.haveAccount}{" "}
                            <button
                                onClick={() => setMode("login")}
                                className="text-cyan-400 hover:underline"
                            >
                                {t.login.signInLink}
                            </button>
                        </>
                    )}
                </p>
            </motion.div>

            {/* Footer */}
            <p className="text-gray-500 text-xs mt-8 text-center">
                {t.login.termsNotice}
            </p>
        </div>
    );
}
