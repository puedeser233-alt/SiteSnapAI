export type AppLanguage = 'en' | 'es';
export type AppTranslations = typeof appTranslations.en;

// Helper to get/set language from localStorage
export const getStoredLanguage = (): AppLanguage => {
    if (typeof window === 'undefined') return 'en';
    return (localStorage.getItem('sitesnap-language') as AppLanguage) || 'en';
};

export const setStoredLanguage = (lang: AppLanguage) => {
    if (typeof window !== 'undefined') {
        localStorage.setItem('sitesnap-language', lang);
    }
};

// Get language from URL param
export const getLanguageFromUrl = (): AppLanguage | null => {
    if (typeof window === 'undefined') return null;
    const urlParams = new URLSearchParams(window.location.search);
    const lang = urlParams.get('lang');
    if (lang === 'en' || lang === 'es') return lang;
    return null;
};
